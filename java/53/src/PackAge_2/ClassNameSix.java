public class ClassNameSix {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        if (a == b) {
            System.out.println("相等");
        }
        else {
            System.out.println("不等");
        }
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
    }
}
